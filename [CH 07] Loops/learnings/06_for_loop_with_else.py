# here i learnt a not famous topic for loop with else

l = [1,2,3,4,5,6,7,8,9]                 # this is list using for taking example

for i in l:                             # for loop intialised for list

    print(i)                            # printing the components of the list 

else:                                   # else can also be used after loop

    print("DONE PRINTING THE LIST")     # when loop exhausts it will run printing the statement