# here i learnt format method (old and not used nowdays) in python


str1 = "{} is a bad {}".format("gurkirat","boy")         # old alternative when there is no f strings

# output :
# gurkirat is a bad boy

print(str1)                                              # printing the string

str1 = "{1} is a bad {0}".format("gurkirat","boy")

# output :
# boy is a bad gurkirat

print(str1)                                              # printing the string
