# here i learnt methods of sets

set_ = {1,2,3,4,2,43,42,4,24,44,223,3455,11,2,1,23,2,4,5,5} # this is a set 

set_.add(533)                                               # adds data into sets   

print(set_)                                                 # prints the set and proof that data actual updated or not

set_.remove(2)                                              # removes the data from the set (raise error if element not found)

print(set_)                                                 # prints the updated set 

set_.discard(3)                                             # Removes  element from  set if it exists If the element is not found then no error is raised

print(set_)                                                 # prints the updated set 

print(len(set_))                                            # print the number of elements present in set

set_.clear()                                                # clear the set or remove everything

print(set_)                                                 # result empty set


