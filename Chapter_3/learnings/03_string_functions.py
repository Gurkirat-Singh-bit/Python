Name = "Gurkirat Singh"                      # intialising string

print(len(Name))                             # len() function is used for finding the length of string

print(Name.endswith("Singh"))                # endswith("") function is used for checking whether a string ends with 
                                             # Given charcater or not

print(Name.startswith("Gurki"))              # startswith("") function is used for checking whether a string starts with 
                                             # Given charcater or not

print(Name.capitalize())                     # capitalize the starting character of  string

print(Name.find("Singh"))                    # find the position of particular word in string -1 indicates there 
                                             # not that particular character

print(Name.replace("Gurkirat", "jashan"))    # replace is used to replace old word with new word or character

print(Name.count("r"))                       # count the occurence of a particular word 


