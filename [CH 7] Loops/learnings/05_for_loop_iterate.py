# here i learnt how to use for loop for iteration in diifferet dataypes

l = [1,2,3,45,6,3,4,67,"gurkirat","dhruv"]               # list with different data

for i in l:                                              # for loop for printing the components of list
    print(i)

t = (123.4,234,5654,"sheetal", "harshit",True)           # tuple with different data

for i in t:                                              # for loop for printing the components of tuple    
    print(i) 

s = "gurkirat singh"                                     # string with my name

for i in s:                                              # for loop for printing the characters of string
    print(i)